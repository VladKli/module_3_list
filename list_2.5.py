# Дано два масиви : А[n] и B[m]. Необхідно створити третій масив, в якому потрібно зібрати:
# Знайти суму всіх від’ємних елементів масиву А, яких немає в масиві В.

A = [1, -0.25, 1, 555, -6, 7, -1, -3, 6, 2, 0]
B = [1, 7, 6, 1, 33, -29]

new_list = [num for num in A if (num < 0) and (num not in B)]

print(sum(new_list))
